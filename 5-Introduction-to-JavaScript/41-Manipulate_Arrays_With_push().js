// In this lesson, I learned about the push() function. .push() can take in one or more
// parameters and push them to the end of the array without replacing what was already inside
// the array.

// Example
var ourArray = ["Stimpson", "J", "cat"];
ourArray.push(["happy", "joy"]);
// ourArray now equals ["Stimpson", "J", "cat", ["happy", "joy"]]

// Setup
var myArray = [["John", 23], ["cat", 2]];

