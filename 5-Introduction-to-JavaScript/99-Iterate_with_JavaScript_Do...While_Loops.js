// I learned about the do...while loop. It's used to run the same code multiple times. First will be "do" for one pass
// and then it runs "while," the specified condition until it's no longer true.

// Setup
var myArray = [];
var i = 10;

// Only change code below this line.

do {
  myArray.push(i);
  i++;
} while (i < 5);
