// In this lesson, I learned what the .pop() function was. It "pops" the value off
// of the end of an array and then returns the "popped off" element.

// Example
var ourArray = [1,2,3];
var removedFromOurArray = ourArray.pop();
// removedFromOurArray now equals 3, and ourArray now equals [1,2]

// Setup
