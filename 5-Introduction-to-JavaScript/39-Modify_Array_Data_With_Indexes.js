// In this lesson, I learned how to modify array data with indexes. Arrays are mutable, meaning
// they can change. By assigning 45 to index 0 in myArray, it took the place of 18 which was previously in that index.

// Example
var ourArray = [18,64,99];
ourArray[1] = 45; // ourArray now equals [18,45,99].

// Setup
var myArray = [18,64,99];

// Only change code below this line.
myArray[0] = 45;
